package entites;

public class Matiere {
	private String codeM;
	private String libM;
	public Matiere() {

	}
	public Matiere(String codeM, String libM) {
		this.codeM = codeM;
		this.libM = libM;
	}
	public String getCodeM() {
		return codeM;
	}
	public void setCodeM(String codeM) {
		this.codeM = codeM;
	}
	public String getLibM() {
		return libM;
	}
	public void setLibM(String libM) {
		this.libM = libM;
	}




}
